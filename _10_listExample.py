# "Bmw, Mercedes, Opel, Mazda" dan oluşan liste kaç elemanlıdır, ilk ve son elemanı nedir?

cars=["Bmw","Mercedes","Opel","Mazda"]
print("Arabalar listesi: {}".format(cars))
print("Listenin Uzunluğu: {}".format(len(cars)))
print("Listenin ilk elemanı: {}".format(cars[0]))
print("Listenin son elemanı: {}".format(cars[len(cars)-1]))